#!/usr/bin/env node
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve as resolvePath } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolvePath(__dirname, "..");
const contractPath = resolvePath(repoRoot, "contracts/model-tiers.ts");

function parseArgs(argv) {
  const outIndex = argv.indexOf("--out");
  return {
    outPath:
      outIndex >= 0 && argv[outIndex + 1]
        ? resolvePath(process.cwd(), argv[outIndex + 1])
        : null,
  };
}

function readRegistrySource() {
  return readFileSync(contractPath, "utf-8");
}

export function extractModelTierRegistry(source) {
  const registryMatch = source.match(
    /export const MODEL_TIER_REGISTRY:[\s\S]*?=\s*\[([\s\S]*?)\]\s*as const;/m,
  );
  if (!registryMatch) {
    throw new Error(
      "Could not find MODEL_TIER_REGISTRY in contracts/model-tiers.ts",
    );
  }

  const objectBlocks = [...registryMatch[1].matchAll(/\{[^{}]*\}/g)].map(
    (match) => match[0],
  );
  const entries = objectBlocks.map((block) => {
    const fields = Object.fromEntries(
      [...block.matchAll(/([a-zA-Z_][a-zA-Z0-9_]*)\s*:\s*"([^"]*)"/g)].map(
        (match) => [match[1], match[2]],
      ),
    );

    if (!fields.provider || !fields.model || !fields.tier) {
      throw new Error(
        `Malformed MODEL_TIER_REGISTRY entry; expected provider, model, and tier string fields: ${block}`,
      );
    }

    return {
      provider: fields.provider,
      model: fields.model,
      tier: fields.tier,
    };
  });

  if (entries.length === 0) {
    throw new Error(
      "MODEL_TIER_REGISTRY did not contain any parseable entries",
    );
  }

  return entries;
}

function validateRegistry(entries) {
  const seen = new Set();
  const wildcards = [];

  for (const entry of entries) {
    if (!["frontier", "mid", "local"].includes(entry.tier)) {
      throw new Error(
        `Invalid model tier '${entry.tier}' for ${entry.provider}/${entry.model}`,
      );
    }

    const key = `${entry.provider}\0${entry.model}`;
    if (seen.has(key)) {
      throw new Error(
        `Duplicate model tier entry for ${entry.provider}/${entry.model}`,
      );
    }
    seen.add(key);

    if (entry.model === "*") {
      wildcards.push(entry.provider);
    }
  }

  const invalidWildcards = wildcards.filter(
    (provider) => provider !== "openai_compatible",
  );
  if (invalidWildcards.length > 0) {
    throw new Error(
      `Only openai_compatible may use a wildcard model; found ${invalidWildcards.join(", ")}`,
    );
  }
}

function quoteElixirString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

export function renderElixirModelTiers(entries) {
  validateRegistry(entries);

  const rows = entries
    .map(
      (entry) =>
        `    {"${quoteElixirString(entry.provider)}", "${quoteElixirString(entry.model)}", :${entry.tier}},`,
    )
    .join("\n");

  return `# Generated by parallel-agent-platform/scripts/generate-runtime-model-tiers.mjs.
# Do not edit by hand.

defmodule SymphonyElixir.ModelTiers do
  @moduledoc """
  Runtime mirror of Platform's contracts/model-tiers.ts registry.
  """

  @type tier :: :frontier | :mid | :local

  @registry [
${rows}
  ]

  @spec tier_of(String.t(), String.t()) :: tier() | nil
  def tier_of(provider, model) when is_binary(provider) and is_binary(model) do
    exact =
      Enum.find(@registry, fn {entry_provider, entry_model, _tier} ->
        entry_provider == provider and entry_model == model
      end)

    wildcard =
      Enum.find(@registry, fn {entry_provider, entry_model, _tier} ->
        entry_provider == provider and entry_model == "*"
      end)

    case exact || wildcard do
      {_provider, _model, tier} -> tier
      nil -> nil
    end
  end
end
`;
}

function main() {
  const { outPath } = parseArgs(process.argv.slice(2));
  const entries = extractModelTierRegistry(readRegistrySource());
  const output = renderElixirModelTiers(entries);

  if (outPath) {
    mkdirSync(dirname(outPath), { recursive: true });
    writeFileSync(outPath, output);
    console.log(`Wrote ${outPath}`);
    return;
  }

  process.stdout.write(output);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
