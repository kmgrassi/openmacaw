#!/usr/bin/env node
import { spawn } from "node:child_process";
import { mkdir, mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const targetPath = path.join(repoRoot, "packages/supabase-schema/src/database.types.ts");
const generatedHeader = "// Generated by `pnpm run db:schema:sync`. Do not edit by hand.\n\n";

await loadDotEnv(path.join(repoRoot, ".env"));

const generated = await generateSupabaseTypes();
await mkdir(path.dirname(targetPath), { recursive: true });
await writeFile(targetPath, `${generatedHeader}${generated}`);
console.log(`Generated Supabase schema artifact at ${targetPath}`);

async function loadDotEnv(envPath) {
  const content = await readFile(envPath, "utf8").catch(() => "");
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) continue;
    const index = trimmed.indexOf("=");
    const key = trimmed.slice(0, index).trim();
    const value = trimmed
      .slice(index + 1)
      .trim()
      .replace(/^['"]|['"]$/g, "");
    if (key && process.env[key] === undefined) process.env[key] = value;
  }
}

async function generateSupabaseTypes() {
  const projectId = process.env.SUPABASE_PROJECT_ID?.trim();
  if (!projectId) {
    throw new Error("SUPABASE_PROJECT_ID is required to generate Supabase schema types.");
  }

  const tempDirectory = await mkdtemp(path.join(tmpdir(), "platform-supabase-schema-"));
  const outputPath = path.join(tempDirectory, "database.types.ts");
  try {
    await run("pnpm", [
      "exec",
      "supabase",
      "gen",
      "types",
      "typescript",
      "--project-id",
      projectId,
      "--schema",
      "public,authz",
    ], outputPath);
    return await readFile(outputPath, "utf8");
  } finally {
    await rm(tempDirectory, { force: true, recursive: true });
  }
}

async function run(command, args, outputPath) {
  await new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: repoRoot,
      env: process.env,
      stdio: ["ignore", "pipe", "inherit"],
    });
    let output = "";
    child.stdout.setEncoding("utf8");
    child.stdout.on("data", (chunk) => {
      output += chunk;
    });
    child.on("error", reject);
    child.on("close", async (code) => {
      if (code !== 0) {
        reject(new Error(`${command} ${args.join(" ")} exited with code ${code}`));
        return;
      }
      try {
        await writeFile(outputPath, output);
        resolve();
      } catch (error) {
        reject(error);
      }
    });
  });
}
