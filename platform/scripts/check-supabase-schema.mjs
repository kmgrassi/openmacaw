#!/usr/bin/env node
import { existsSync } from "node:fs";
import { mkdtemp, readdir, readFile, rm, writeFile } from "node:fs/promises";
import { spawn } from "node:child_process";
import { homedir, tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
);
const targetPath = path.join(
  repoRoot,
  "packages/supabase-schema/src/database.types.ts",
);
const legacyPath = path.join(repoRoot, "supabase/generated/database.types.ts");
const generatedHeader =
  "// Generated by `pnpm run db:schema:sync`. Do not edit by hand.\n\n";
const expectedExtensions = ["pgcrypto"];

const failures = [];

await loadDotEnv(path.join(repoRoot, ".env"));
const psqlUrl =
  process.env.SUPABASE_DB_URL?.trim() || process.env.DATABASE_URL?.trim();

const schemaSource = await checkGeneratedArtifact();
await checkDeprecatedGeneratedImports();
await checkLiveSchemaArtifacts(schemaSource);
await checkLiveDatabaseDiagnostics();

if (failures.length > 0) {
  for (const failure of failures) {
    emit("error", {
      event: "database_schema_drift_detected",
      ...failure,
    });
  }
  process.exit(1);
}

emit("info", {
  event: "database_schema_diagnostic_summary",
  status: "passed",
  artifact: "packages/supabase-schema/src/database.types.ts",
});

async function checkGeneratedArtifact() {
  if (existsSync(legacyPath)) {
    fail({
      check: "legacy_generated_types_absent",
      artifact: "supabase/generated/database.types.ts",
      owning_artifact: "packages/supabase-schema/src/database.types.ts",
      message: "Deprecated generated Supabase type file still exists.",
    });
  } else {
    pass("legacy_generated_types_absent", {
      artifact: "supabase/generated/database.types.ts",
    });
  }

  const schemaSource = await readFile(targetPath, "utf8").catch((error) => {
    fail({
      check: "generated_types_readable",
      artifact: "packages/supabase-schema/src/database.types.ts",
      message: `Could not read generated Supabase types: ${error.message}`,
    });
    return "";
  });

  if (!schemaSource) return "";

  if (!schemaSource.includes("export type Database")) {
    fail({
      check: "generated_types_shape",
      artifact: "packages/supabase-schema/src/database.types.ts",
      message: "Generated Supabase type artifact does not export Database.",
    });
  } else {
    pass("generated_types_shape", {
      artifact: "packages/supabase-schema/src/database.types.ts",
    });
  }

  if (!schemaSource.startsWith(generatedHeader)) {
    fail({
      check: "generated_types_header",
      artifact: "packages/supabase-schema/src/database.types.ts",
      expected_value: generatedHeader.trim(),
      message:
        "Generated Supabase type artifact is missing the generated-file header.",
    });
  } else {
    pass("generated_types_header", {
      artifact: "packages/supabase-schema/src/database.types.ts",
    });
  }

  return schemaSource;
}

async function checkDeprecatedGeneratedImports() {
  const offenders = [];
  for (const directory of [
    "apps/api/src",
    "apps/web/src",
    "contracts",
    "packages",
  ]) {
    const absoluteDirectory = path.join(repoRoot, directory);
    if (!existsSync(absoluteDirectory)) continue;
    for (const file of await walk(absoluteDirectory)) {
      if (file === targetPath) continue;
      const content = await readFile(file, "utf8");
      if (content.includes("supabase/generated/database.types")) {
        offenders.push(path.relative(repoRoot, file));
      }
    }
  }

  if (offenders.length > 0) {
    for (const artifact of offenders) {
      fail({
        check: "deprecated_generated_type_import",
        artifact,
        owning_artifact: "packages/supabase-schema/src/database.types.ts",
        message: "File imports the deprecated local Supabase schema artifact.",
      });
    }
    return;
  }

  pass("deprecated_generated_type_import", {
    artifact: "packages/supabase-schema/src/database.types.ts",
  });
}

async function checkLiveSchemaArtifacts(schemaSource) {
  const projectId = process.env.SUPABASE_PROJECT_ID?.trim();
  const hasSupabaseAuth =
    Boolean(process.env.SUPABASE_ACCESS_TOKEN?.trim()) ||
    existsSync(path.join(homedir(), ".supabase/access-token"));

  if (!projectId || !hasSupabaseAuth) {
    const missing = [
      projectId ? null : "SUPABASE_PROJECT_ID",
      hasSupabaseAuth ? null : "Supabase auth",
    ]
      .filter(Boolean)
      .join(" and ");
    skip("live_generated_type_freshness", {
      artifact: "packages/supabase-schema/src/database.types.ts",
      reason: `${missing} not configured`,
    });
    return;
  }

  const generatedSource = await generateSupabaseTypes(projectId).catch(
    (error) => {
      fail({
        check: "live_generated_type_freshness",
        artifact: "packages/supabase-schema/src/database.types.ts",
        project_id: projectId,
        message: `Could not generate Supabase schema types from project ${projectId}: ${error.message}`,
      });
      return "";
    },
  );
  if (!generatedSource) return;

  const normalizedSchemaSource = schemaSource.startsWith(generatedHeader)
    ? schemaSource.slice(generatedHeader.length)
    : schemaSource;
  if (normalizedSchemaSource !== generatedSource) {
    fail({
      check: "live_generated_type_freshness",
      artifact: "packages/supabase-schema/src/database.types.ts",
      owning_artifact: "harper-server/supabase/migrations",
      project_id: projectId,
      message:
        "Generated Supabase types are stale relative to the configured Supabase project.",
    });
    return;
  }

  pass("live_generated_type_freshness", {
    artifact: "packages/supabase-schema/src/database.types.ts",
    project_id: projectId,
  });
}

async function checkLiveDatabaseDiagnostics() {
  if (!psqlUrl) {
    skip("live_database_catalog", {
      artifact: "configured Supabase database",
      reason:
        "SUPABASE_DB_URL or DATABASE_URL not configured; skipping live extension catalog checks.",
    });
    return;
  }

  const catalog = await queryPostgresJson(`
    select jsonb_build_object(
      'extensions', (
        select coalesce(jsonb_agg(extname order by extname), '[]'::jsonb)
        from pg_extension
      ),
      'schemaVersions', '[]'::jsonb
    )::text;
  `).catch((error) => {
    fail({
      check: "live_database_catalog",
      artifact: "configured Supabase database",
      message: `Could not read live database catalog: ${error.message}`,
    });
    return null;
  });
  if (!catalog) return;

  const installedExtensions = new Set(catalog.extensions ?? []);
  for (const extension of expectedExtensions) {
    if (!installedExtensions.has(extension)) {
      fail({
        check: "expected_extension_installed",
        artifact: "supabase/config.toml",
        expected_value: extension,
        message:
          "Expected Postgres extension is not installed in the live database.",
      });
    } else {
      pass("expected_extension_installed", {
        artifact: "supabase/config.toml",
        extension,
      });
    }
  }
}

async function walk(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    if (entry.name === "node_modules" || entry.name === "dist") continue;
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) files.push(...(await walk(fullPath)));
    if (entry.isFile() && /\.(ts|tsx)$/.test(entry.name)) files.push(fullPath);
  }
  return files;
}

async function loadDotEnv(envPath) {
  const content = await readFile(envPath, "utf8").catch(() => "");
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) continue;
    const index = trimmed.indexOf("=");
    const key = trimmed.slice(0, index).trim();
    const value = trimmed
      .slice(index + 1)
      .trim()
      .replace(/^['"]|['"]$/g, "");
    if (key && process.env[key] === undefined) process.env[key] = value;
  }
}

async function generateSupabaseTypes(projectId) {
  const tempDirectory = await mkdtemp(
    path.join(tmpdir(), "platform-supabase-schema-check-"),
  );
  const outputPath = path.join(tempDirectory, "database.types.ts");
  try {
    await run(
      "pnpm",
      [
        "exec",
        "supabase",
        "gen",
        "types",
        "typescript",
        "--project-id",
        projectId,
        "--schema",
        "public,authz",
      ],
      outputPath,
    );
    return await readFile(outputPath, "utf8");
  } finally {
    await rm(tempDirectory, { force: true, recursive: true });
  }
}

async function queryPostgresJson(sql) {
  const outputPath = path.join(
    await mkdtemp(path.join(tmpdir(), "platform-supabase-catalog-")),
    "catalog.json",
  );
  try {
    await run(
      "psql",
      [psqlUrl, "--no-psqlrc", "--tuples-only", "--no-align", "--command", sql],
      outputPath,
    );
    const output = (await readFile(outputPath, "utf8")).trim();
    return JSON.parse(output);
  } finally {
    await rm(path.dirname(outputPath), { force: true, recursive: true });
  }
}

async function run(command, args, outputPath) {
  await new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: repoRoot,
      env: process.env,
      stdio: ["ignore", "pipe", "inherit"],
    });
    let output = "";
    child.stdout.setEncoding("utf8");
    child.stdout.on("data", (chunk) => {
      output += chunk;
    });
    child.on("error", reject);
    child.on("close", async (code) => {
      if (code !== 0) {
        reject(
          new Error(
            `${command} ${redactedArgs(args).join(" ")} exited with code ${code}`,
          ),
        );
        return;
      }
      try {
        await writeFile(outputPath, output);
        resolve();
      } catch (error) {
        reject(error);
      }
    });
  });
}

function pass(check, fields = {}) {
  emit("info", {
    event: "database_schema_diagnostic_check",
    check,
    status: "passed",
    ...fields,
  });
}

function skip(check, fields = {}) {
  emit("warn", {
    event: "database_schema_diagnostic_check",
    check,
    status: "skipped",
    ...fields,
  });
}

function fail(fields) {
  failures.push(fields);
}

function emit(level, payload) {
  const output = {
    level,
    timestamp: new Date().toISOString(),
    service: "platform-schema-diagnostics",
    environment: process.env.APP_ENV ?? process.env.NODE_ENV ?? "local",
    ...payload,
  };
  const line = `${JSON.stringify(output)}\n`;
  if (level === "error") process.stderr.write(line);
  else process.stdout.write(line);
}

function redactedArgs(args) {
  return args.map((arg, index) => {
    if (arg === psqlUrl) return "[redacted-db-url]";
    const previous = args[index - 1];
    if (previous === "--password" || previous === "--db-url")
      return "[redacted]";
    return arg;
  });
}
